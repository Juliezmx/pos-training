package app;

public class ClsMagneticCardFieldFormat {
	private String m_sFieldKey;
	private int m_iReadStartPos;
	private int m_iReadLength;
	private String m_sFieldTokenChar;
	private String m_sPrefixStringChecking;
	
	public ClsMagneticCardFieldFormat(){
		m_sFieldKey = "";
		m_iReadStartPos = -1;
		m_iReadLength = -1;
		m_sFieldTokenChar = "";
		m_sPrefixStringChecking = "";
	}
	
	public void setFieldKey(String sFieldKey){
		m_sFieldKey = sFieldKey;
	}
	
	public void setReadStartPos(int iStartPos){
		m_iReadStartPos = iStartPos;
	}
	
	public void setReadLength(int iLength){
		m_iReadLength = iLength;
	}
	
	public void setFieldTokenChar(String sTokenChar){
		m_sFieldTokenChar = sTokenChar;
	}
	
	public void setPrefixStringChecking(String sPrefixStringChecking){
		m_sPrefixStringChecking = sPrefixStringChecking;
	}
	
	public String getFieldKey(){
		return m_sFieldKey;
	}
	
	public int getReadStartPos(){
		return m_iReadStartPos;
	}
	
	public int getReadLength(){
		return m_iReadLength;
	}
	
	public String getFieldTokenChar(){
		return m_sFieldTokenChar;
	}
	
	public String getPrefixStringChecking(){
		return m_sPrefixStringChecking;
	}
}
