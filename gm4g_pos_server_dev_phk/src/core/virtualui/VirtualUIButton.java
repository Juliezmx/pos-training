package core.virtualui;


public class VirtualUIButton extends VirtualUIBasicElement {
	
	public VirtualUIButton() {
		super(HeroActionProtocol.View.Type.BUTTON);
		
		super.setGradient(true);
		super.setCornerRadius(5);
		
		VirtualUIEvent oEvent = new VirtualUIEvent(VirtualUIEvent.EVENT_CLICK);
		super.addEvent(oEvent);
    }
}
